package free_app;

public class Free_app {

	private int free_app_ID;
	private String free_app_Title;
	private String userID;
	private String free_app_Date;
	private String free_app_Content;
	private int free_app_Available;
	
	
	public int getFree_app_ID() {
		return free_app_ID;
	}
	public void setFree_app_ID(int free_app_ID) {
		this.free_app_ID = free_app_ID;
	}
	public String getFree_app_Title() {
		return free_app_Title;
	}
	public void setFree_app_Title(String free_app_Title) {
		this.free_app_Title = free_app_Title;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getFree_app_Date() {
		return free_app_Date;
	}
	public void setFree_app_Date(String free_app_Date) {
		this.free_app_Date = free_app_Date;
	}
	public String getFree_app_Content() {
		return free_app_Content;
	}
	public void setFree_app_Content(String free_app_Content) {
		this.free_app_Content = free_app_Content;
	}
	public int getFree_app_Available() {
		return free_app_Available;
	}
	public void setFree_app_Available(int free_app_Available) {
		this.free_app_Available = free_app_Available;
	}
	
	
	
	
}
